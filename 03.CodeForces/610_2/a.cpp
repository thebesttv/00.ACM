typedef long long LL;
typedef long long unsigned LLU;

LL a, b, c, r;

int main(void){
  int T; cin >> T;
  while(T--){
    cin >> a >> b >> c >> r;
    if(a > b) swap(a,b);
    LL tl = max(c - r, a);
    LL tr = min(c + r, b);
    LL ans;
    if(tl > tr) ans = b - a;
    else ans = b - a - (tr-tl);
    cout << ans << endl;
  }
  return 0;
}
