const int MAX = 4e5 + 20;
int q,x,cnt[MAX];
int tot = 0, cur = 0;

void update(int y){
  ++cnt[y];
  while(cnt[cur]){
    ++tot; --cnt[cur];
    cur = (cur + 1) % x;
  }
}

int main(void){
  scanf("%d%d",&q,&x);
  int y;
  while(q--){
    scanf("%d",&y);
    update(y % x);
    printf("%d\n",tot);
  }

  return 0;
}
