int read(){
  int s = 0, ch;
  do ch = getchar(); while(!isdigit(ch));
  while(isdigit(ch)) s = s*10 + ch-'0', ch = getchar();
  return s;
}

int nread(){
  int s = 0, ch, neg = 0;
  do ch = getchar(), neg |= (ch == '-'); while(!isdigit(ch));
  while(isdigit(ch)) s = s*10 + ch-'0', ch = getchar();
  return neg ? -s : s;
}

void go(){
  int cnt = 1, ch;
  while(ch = getchar()){
    if(ch == '6') ++cnt;
    else{
      if(cnt > 9){
        printf("27");
      }else if(cnt > 3){
        printf("9");
      }else{
        while(cnt--) putchar('6');
      }
      if(ch == EOF) exit(0);
      else putchar(ch);
      break;
    }
  }
}

int main(void){
  int ch;
  while((ch = getchar())!=EOF){
    if(ch == '6') go();
    else putchar(ch);
  }

  return 0;
}

